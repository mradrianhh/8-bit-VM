#ifndef _8BITVM_VREGISTERS_H_
#define _8BITVM_VREGISTERS_H_

#include <stdint.h>

#define BIT0 (uint8_t)(0b00000001)
#define BIT1 (uint8_t)(0b00000010)
#define BIT2 (uint8_t)(0b00000100)
#define BIT3 (uint8_t)(0b00001000)
#define BIT4 (uint8_t)(0b00010000)
#define BIT5 (uint8_t)(0b00100000)
#define BIT6 (uint8_t)(0b01000000)
#define BIT7 (uint8_t)(0b10000000)
#define BIT(x) ((uint8_t)1 << (x))

#define MAX_REGISTERS   (10)

typedef uint8_t REGISTER_t;
typedef char LOGICAL_REGISTER_t[4];

typedef REGISTER_t REGISTER_FILE_t[MAX_REGISTERS];
typedef LOGICAL_REGISTER_t LOGICAL_REGISTER_FILE_t[MAX_REGISTERS];

enum REGISTER_ADDRESS
{
    REGISTER_ADDRESS_R0 = 0,
    REGISTER_ADDRESS_R1,
    REGISTER_ADDRESS_R2,
    REGISTER_ADDRESS_R3,
    REGISTER_ADDRESS_PC,
    REGISTER_ADDRESS_LR,
    REGISTER_ADDRESS_SP,
    REGISTER_ADDRESS_IR,
    REGISTER_ADDRESS_MAR,
    REGISTER_ADDRESS_MDR,
};

#endif